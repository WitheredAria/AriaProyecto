# Ejercicio 1
# Campo de introducción de nombre.
nombre = input("Introduce tu nombre: ");
# Campo de introducción de edad en número entero
edad = int(input("Introduce tu edad: "));
# Campo de introducción de altura en número decimal
altura = float(input("Introduce tu altura en metros: "));
# Muestra los resultados de los campos en pantalla
print("Tu nombre es", nombre, ", tienes", edad, "años y mides", altura, "metros de alto.");

# Ejercicio 2
# Campo de introducción de primer número
num1 = int(input("Introduce un número: "));
# Campo de introducción de segundo número
num2 = int(input("Introduce otro número: "));
# Tercera variable que sumará las primeras dos
suma = num1 + num2;
# Muestra el resultado de la operación hecha por la tercera variable
print("La suma de", num1, "+", num2, "es =", suma);

# Ejercicio 3
# Campo de introducción del radio del círculo
radio = float(input("Introduce el radio de un círculo: "));
